public class TablaMultiplicar {
	//Pintar la tabla de multiplicar de un numero
	public static void main(String[] args) {
		
		int numero = 6;
		for (int i = 1; i <= 10; i++) {
			System.out.println(i + "X" + numero +"="+ (i*numero));
		}
		
	}

}
